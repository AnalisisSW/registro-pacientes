    /**
     * Método para la validar los Campos
     * @param codigo
     * @param apellido
     * @param nombre
     * @return boolean
     */
    private boolean validarCampos(String codigo, String apellido, String nombre) {
        return (codigo!=null && !codigo.isEmpty() && apellido!=null && 
                !apellido.isEmpty() && nombre!=null && !nombre.isEmpty());
    }

